package mypackage;

public class PracticeSwitch {

	public static void main(String[] args) 
	{
      int a=2;
      switch(a)
      { 
      case 1:
    	  System.out.println("print 1");
    	  break;
      case 2:
    	  System.out.println("print 2");
    	  break;
      case 3:
    	  System.out.println("print 3");
    	  break;
      case 4:
    	  System.out.println("print 4");
    	  break;
      default:
    	  System.out.println("print default");
    	  break;
	}

}
}
