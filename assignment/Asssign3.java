package assignment;

public class Asssign3 {

	public class StaticMethodsExample {

		  // Static method 1
		  public static void printMessage1() {
		    System.out.println("This is message from method 1");
		  }

		  // Static method 2
		  public static void printMessage2() {
		    System.out.println("This is message from method 2");
		  }

		  // Static method 3
		  public static void printMessage3() {
		    System.out.println("This is message from method 3");
		  }

		  // Static method 4
		  public static void printMessage4() {
		    System.out.println("This is message from method 4");
		  }

		  public static void main(String[] args) {
		    // Call static methods directly using the class name
		    printMessage1();
		    printMessage2();
		    printMessage3();
		    printMessage4();
		  }
		}
}